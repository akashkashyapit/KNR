                                <div class="text-content mb-30">
                                    <p>Step 1: Select most onerous path configuration of the system for the study.</p>
                                    <p>Step 2: Collect, compile and validate data as per data questionnaire.</p>
                                    <p>Step 3: Model the components of the system - Power Transformer, GIS section - OHL section, Surge arresters, cable, etc. & Develop network diagrams for the modelled system for EMTP analysis.</p>
