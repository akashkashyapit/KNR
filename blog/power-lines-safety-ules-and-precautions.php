                Electricity is a great blessing for us as it has advanced and
                automated our life to an infinite extent. But along with these
                benefits, electricity is also very dangerous at the same time.
                It creates a number of hazards and health issues thus
                endangering our life. If we properly follow the safety rules,
                standards, and precautions, then we can reduce and even
                eliminate the risks and hazards caused by electricity.
                Conclusively, it's up to us that either we make our life
                comfortable or full of risks.
