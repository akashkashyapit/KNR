              <h3 class="mt-30">
                There are some precautions which must be followed to avoid
                electrical hazards and risks:
              </h3>

              <ul class="lead-point mt-15">
                <li>
                  <i class="ion-android-done"></i>Animals should never be tied
                  to a transmission tower or pole as it can risk their life.
                </li>
                <li>
                  <i class="ion-android-done"></i>Any object made up of metal
                  or conducting material should not be thrown on overhead
                  lines.
                </li>
                <li>
                  <i class="ion-android-done"></i>During rain, towers or poles
                  must not be touched by any person because the tower body
                  becomes energized due to the conductivity of water.
                </li>
                <li>
                  <i class="ion-android-done"></i>During storms or strong
                  winds, we should keep a safe distance from powerlines as
                  live conductors may accidentally fall over a person.
                </li>
                <li>
                  <i class="ion-android-done"></i>If a person sees any spark
                  on overhead live conductors, his first responsibility should
                  be to inform the relevant authorities to avoid any accident.
                </li>
                <li>
                  <i class="ion-android-done"></i>Any construction work should
                  not be carried out under or near HV power lines.
                </li>
              </ul>
              <h3 class="mt-30">
                Besides the precautions, there are also some safety rules for
                the linemen who work on HV transmission lines. These areas
                following:
              </h3>
              <ul class="lead-point mt-15">
                <li>
                  <i class="ion-android-done"></i>Linemen must be familiar
                  with all the safety rules and regulations.
                </li>
                <li>
                  <i class="ion-android-done"></i>A lineman who is going to do
                  any operation must be trained well. Without any experience
                  or training, he should not try to take any risk as it can
                  put his life in danger.
                </li>
                <li>
                  <i class="ion-android-done"></i>A lineman must be equipped
                  with all necessary PPEs before starting operation.
                </li>
                <li>
                  <i class="ion-android-done"></i>Before the start of work, it
                  should be made sure that the tower on which the lineman is
                  going to work is completely de-energized.
                </li>
                <li>
                  <i class="ion-android-done"></i>If a lineman is not familiar
                  with any tool, then he should never try to use it as it can
                  be risky.
                </li>
                <li>
                  <i class="ion-android-done"></i>A lineman must be in
                  continuous communication with other team members when doing
                  an operation.
                </li>
                <li>
                  <i class="ion-android-done"></i>A lineman must not be in a
                  hurry as it can cause an accident to him or other team
                  members.
                </li>
              </ul>

