            <div class="post-entry">
              <blockquote class="dark-bg">
                “Process of Bringing Insulation Strengths of Electrical
                Equipment into proper relationship with expected Overvoltages
                and characteristics of Surge Protective Devices so as to
                reduce the risk of failure to an economically & operationally
                acceptable level”
              </blockquote>
              <p class="lead">
                Insulation Coordination in High Voltage – Electric power
                supply should ensure reliability and continuity to the utility
                concerns. The power lines and sub-stations are to be operated
                and protected against overvoltages such that the number of
                failures is as few as possible. At the same time, the cost
                involved in the design, installation, and operation of the
                protective devices should not be too high. Hence, a gradation
                of system insulation and protective device operation is to be
                followed, keeping in view the importance of the various
                equipment involved.
              </p>

              <p>
                Generally, sub-stations contain transformers, switchgear, and
                other valuable equipment with non-self restoring Insulation
                Coordination in High Voltage, which have to be protected
                against failures and internal destruction. For other
                apparatus, which contain self-restoring insulation, like
                string insulators, they may be allowed to flashover in air.
                But the flashovers should be kept to a minimum so that the
                system disturbances are the least. Hence, lightning and
                switching surge protection requires establishment of
                protective voltage levels called shunt protection levels
              </p>
              <p>
                The lightning impulse withstand level known as the Basic
                Impulse Level (BIL) is established for each system nominal
                voltage for different apparatus. Various equipment and their
                component parts should have their BIL above the system
                protective level, by a suitable margin. This margin is usually
                determined with respect to air insulation by statistical
                methods. For non-self-restoring Insulation Coordination in
                High Voltage like the transformer insulation, the margin limit
                is fixed using conventional methods.
              </p>
              <p>
                For system voltages below 400 kV, the switching surges are not
                of importance. If the BIL is chosen correctly, relative to the
                prevailing protective level, the equipment will have an
                adequate switching surge level. For higher system voltages,
                since the switching surges are of higher magnitude compared to
                the lightning overvoltages, switching surge magnitudes are of
                importance and the following criterion is to be adopted.
              </p>
              <p>
                1.The flashover voltage of a protective derive is chosen such
                that it will not operate for switching overvoltages and other
                power frequency and its harmonic overvoltages. But other long
                duration overvoltages, namely, sustained overvoltage due to
                faults and even the above-mentioned overvoltages may sometimes
                cause thermal overloading due to leakage Therefore, the BIL
                has to be higher.
              </p>
              <p>
                2.For EHV systems, it may be economical to use a protective
                device for limiting the overvoltages due to lightning as well
                as switching surges to a particular level. At present, there
                are surge arresters which operate for both types of
                overvoltages mentioned above. In such cases, it is preferable
                to assign to each protected equipment a Switching Impulse
                Level (SIL), so that there is a small margin above the
                controlled switching surge level, so that the surge diverters
                operate on switching surges, only when the controlling devices
                fail. Normally, only rod gaps and lightning arresters are used
                as protective devices for protection, and their
                characteristics are considered here.
              </p>
