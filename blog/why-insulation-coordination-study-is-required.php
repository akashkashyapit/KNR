            <div class="post-entry">
              <p>
                One of the most important and primary requirements in the
                design of the extra high voltage (EHV) / Ultra high voltage
                (UHV) system is the determination of expected overvoltages on
                the various lines / cables and buses constituting the system
                thereby deciding the appropriate protective devices to suit
                the particular characteristics of substation layout.
              </p>

              <blockquote class="dark-bg">
                The objective of insulation coordination study is to verify
                that the already selected equipment are appropriate and it
                satisfies the performance criteria required for overvoltages,
                switching surge and overvoltage caused by lightning.
              </blockquote>
              <p class="lead">
                Insulation Coordination study is carried out in accordance
                with the methodology and guidelines outlined in IEEE standard
                1313.2-1999, IEC 71-2, IEC 71-4, CIGRE WG 33.01, CIGRE WG
                D1.03 recommendations and IEEE technical papers presented in
                various forums.
              </p>
              <h3 class="mt-30">
                The following overvoltage studies are done:
              </h3>

              <ul class="lead-point mt-15">
                <li>
                  <i class="ion-android-done"></i>Lightning Overvoltage (Fast
                  front overvoltage)
                </li>
                <li>
                  <i class="ion-android-done"></i>Switching Overvoltage (Slow
                  front overvoltage)
                </li>
                <li>
                  <i class="ion-android-done"></i>Temporary Overvoltage (Power
                  frequency overvoltage)
                </li>
                <li>
                  <i class="ion-android-done"></i>Very Fast Transients
                  Overvoltage or VFTO (Very fast front overvoltage)
                </li>
              </ul>
              <h3 class="mt-30">LIGHTNING OVERVOLTAGES</h3>
              <p>
                The overvoltages in the substation depend upon the amplitude
                and shape of overvoltage impinging the substation from the
                overhead line conductor as well as the travelling wave
                behavior on the substation itself. The most important
                parameters of impinging overvoltages are its steepness and the
                separation distance between the surge arrester and the
                protected equipment. For insulation co-ordination studies,
                lightning is assumed to hit a nearby tower or the shield wire
                of the incoming line causing a back flashover. The resultant
                lightning surge enters the substation and propagates inside
                depending upon the substation layout. These overvoltages will
                provide the data required for detailed arrester
                specifications.
              </p>

              <h3 class="mt-30">i) SHIELDING FAILURE</h3>
              <p>
                Lightning surge also may enter a substation by direct stroke
                on any of the phases. Shielding failure correspond to those
                direct strokes to the phase conductors of a shielded
                transmission line which occur typically when lightning strokes
                of low magnitude (a few kA) bypass the overhead shield wires.
              </p>

              <h3 class="mt-30">ii) BACK FLASHOVER</h3>
              <p>
                An event of greater concern is back flashovers. Back flashover
                occurs in transmission lines during lightning, when the
                potential of the tower rises with respect to the conductor.
                This causes voltage across the insulator to rise beyond the
                limits resulting in a flashover. The severity of the
                overvoltage waves depends upon the magnitude and the steepness
                of the lightning stroke current.
              </p>

              <h3 class="mt-30">SWITCHING OVERVOLTAGES</h3>
              <p>
                Switching overvoltages (SOV) are generated when a circuit is
                energized, reenergized or opened by a GIS circuit breaker and
                disconnector switch operations. But it is easily limited by
                re-strike free operation of GIS circuit breakers and shunt
                reactors. A satisfactory protection against switching surges
                will normally be obtained if the GIS are well protected
                against lightning overvoltages.
              </p>

              <h3 class="mt-30">TEMPORARY OVERVOLTAGES</h3>
              <p>
                Temporary overvoltages (TOV) can originate from faults,
                switching operations such as load rejection, resonance
                conditions, and nonlinearities or by a combination of these.
                IEEE Standard 1313.1-1996 defines temporary overvoltages as an
                “oscillatory phase-to-ground or phase-to-phase overvoltage of
                relatively long duration at a given location and that is
                undamped or only weakly damped” [12]. These temporary
                overvoltages are characterized by the parameters such as
                amplitude, the oscillation frequency, total duration, etc.
              </p>

              <h3 class="mt-30">VERY FAST TRANSIENT OVERVOLTAGE</h3>
              <p>
                Very fast transient overvoltages (VFTO) arise within a GIS any
                time there is an instantaneous change in voltage. Most often
                this change occurs as a result of the opening or closing of a
                disconnector switch (DS). Other events, such as the operation
                of a circuit breaker (CB), the occurrence of a line-to-ground
                fault or the closing of an earthing switch can also cause
                VFTO. However, during a DS operation a high number of
                re-strikes and pre-strikes occur due to the low operating
                speed of DS compared to a circuit- breaker. Therefore, DS
                switching is the main source for generating VFTO.
              </p>
