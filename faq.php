          <h3>General Questions</h3>
          <div class="panel-body">
            <h4 class="panel-title">Why Insulation Coordination Study?</h4>
            <p>The overall aim of insulation coordination is to reduce to an economically and operationally acceptable level the cost and disturbance caused by insulation failure.</p>


          </div>
          <div class="panel-body">
            <h4 class="panel-title">What are various overvoltage studies conducted while doing insulation Coordination Study?</h4>
            <p>Insulation Coordination study is carried out in accordance with the methodology and guidelines outlined in IEEE standard 1313.2-1999, IEC 71-2, IEC 71-4, CIGRE WG 33.01, CIGRE WG D1.03 recommendations and IEEE technical papers presented in various forums.</p>
            <p>The following overvoltage studies are done: </p>
            <p>a) Lightning Overvoltage (Fast front overvoltage) </p>
            <p>b) Switching Overvoltage (Slow front overvoltage) </p>
            <p>c) Temporary Overvoltage (Power frequency overvoltage) </p>
            <p>d) Very Fast Transients Overvoltage or VFTO (Very fast front overvoltage)</p>
          </div>
          <div class="panel-body">
            <h4 class="panel-title">What is Surge Arrester?</h4>
            <p> As per - IEC - 60099 -1, Surge Arrester is defined as, the device designed to protect the electrical apparatus from high transient voltages and to limit the duration and the amplitude of follow current.</p>
          </div>
          <div class="panel-body">
            <h4 class="panel-title">What are the ideal characteristics of a Surge Arrester?</h4>
            <p>Ideal characteristics of a Surge Arrester are:</p>

            <ul>
              <li>It should not draw any current at normal power frequency voltage.</li>
              <li>It should draw current during transient overvoltage and should provide a low impedance path to ground.</li>
              <li>It must be capable of interrupting the power frequency follow current after the surge is discharged to ground</li>
            </ul>
          </div>
          <div class="panel-body">
            <h4 class="panel-title">Which overvoltage performance studies are required to ascertain Insulation Coordination of the Substation?</h4>
            <p>The following overvoltage performance studies are carried out to ascertain Insulation Coordination of the Substation: </p>
            <ul>
              <li>Temporary Overvoltage</li>
              <li>decides the rating of arrester</li>
              <li>Lightning Overvoltage </li>
              <li>decides number of arresters and their locations</li>
              <li>Switching Overvoltage – for system voltage > 245kV </li>
              <li>decides location of arrester and PIR requirement</li>
              <li>Very Fast Transient Overvoltage – for GIS substations of system voltage > 245kV </li>
            </ul>

